package com.melon.peace.bean;

public class Music {
	private String musicNo;
	private String vocal;
	private String title;
	private String releasComp;
	private String releasDate;
	
	public String getMusicNo() {
		return musicNo;
	}
	public void setMusicNo(String musicNo) {
		this.musicNo = musicNo;
	}
	public String getVocal() {
		return vocal;
	}
	public void setVocal(String vocal) {
		this.vocal = vocal;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getReleasComp() {
		return releasComp;
	}
	public void setReleasComp(String releasComp) {
		this.releasComp = releasComp;
	}
	public String getReleasDate() {
		return releasDate;
	}
	public void setReleasDate(String releasDate) {
		this.releasDate = releasDate;
	}
	

}
